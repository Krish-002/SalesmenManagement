     public void populateJtable()
    {
        /*
        this method essentially gets the list of orders that have their 
        status as pendning approval, which are then arranged in the
        jtable using a for loop
        */
    RetailerOrder_DB Order = new RetailerOrder_DB();
        ArrayList<RetailerOrder_DB> OrderList = Order.OrderList("Order Pending Approval");
        
        String[] colNames = {"Order ID", "Salesmen Email","Date","Quantity","Brand","Product Details","Retailer Email","Order Status"};
        Object[][] rows = new Object[OrderList.size()][8];

        
        for(int i = 0; i < OrderList.size(); i++){
          
            rows[i][0] = OrderList.get(i).getID();
            rows[i][1] = OrderList.get(i).getSalesmenEmail();
            rows[i][2] = OrderList.get(i).getDateOfSale();
            rows[i][3] = OrderList.get(i).getQuantity();
            rows[i][4] = OrderList.get(i).getBrand();
            rows[i][5] = OrderList.get(i).getProductDetails();
            rows[i][6] = OrderList.get(i).getRetailerEmail();
            rows[i][7] = OrderList.get(i).getOrderStatus();
          
        }
        
        DefaultTableModel mmodel = new DefaultTableModel(rows, colNames);
        jTable_Approval.setModel(mmodel);
        jTable_Approval.setRowHeight(45);
    }

