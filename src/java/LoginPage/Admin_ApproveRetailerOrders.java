    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
    
        /*
        This section of the code, is used to program the update button
        First the values from the text fields are fetched
        and using the update query from MYSQL the corresponding
        values are update in the database. This is kept in a try catch loop 
        to avoid any exceptions.
        */
        
                String SalesmenEmail = TF_SalesmenEmail.getText();
                String OrderStatus = TF_OrderStatus.getText();
                int ID = Integer.parseInt(OrderID_TF.getText());
                String DateOfSales = Date_TF.getText();
                String ProductDetails = jTextField_Email.getText();
                String Brand = Brand_TF.getText();
                int Quantity = Integer.parseInt(Quantity_TF.getText());
                String RetailerEmail = TF_RE.getText();
                
                

                Connection con = My_CNX.getConnection();
                PreparedStatement ps;
                try {
                  
                    ps = con.prepareStatement("UPDATE `retailerorder` SET `SalesmenEmail`=?,"
                            + "`DateOfSale`=?,`Quantity`=?,`Brand`=?,`ProductDetails`=?,`RetailerEmail`=?,"
                            + "`OrderStatus`=? WHERE `ID` = ?");
                    ps.setString(1, SalesmenEmail);
                    ps.setString(2, DateOfSales);
                    ps.setInt(3, Quantity);
                    ps.setString(4, Brand);
                    ps.setString(5, ProductDetails);
                    ps.setString(6, RetailerEmail);
                    ps.setString(7, OrderStatus);
                    ps.setInt(8, ID);

            if(ps.executeUpdate() != 0){
                JOptionPane.showMessageDialog(null, "Order Status Updated");               
                }
                else{
                    JOptionPane.showMessageDialog(null, "Something Wrong, Try Agin!");                   
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(C_Users.class.getName()).log(Level.SEVERE, null, ex);
        }
                
                populateJtable(); //populate the jtable with the updated values
               
            
    }//GEN-LAST:event_jButton_UpdateActionPerformed

