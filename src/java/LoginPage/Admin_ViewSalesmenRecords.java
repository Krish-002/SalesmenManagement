    public void populateJtable()
    {
        //Uses the Salesmen List method created in Salesmen_DB class
        //Loop through the array list and populate jtable (on the basis of sales and target)
    SalesmenSale_DB Salesmen = new SalesmenSale_DB();
        ArrayList<SalesmenSale_DB> AllSalesmenSaleList = Salesmen.AllSalesmenSaleList();
        
        String[] colNames = {"Email","Total Sales", "Sales Target"};
        Object[][] rows = new Object[AllSalesmenSaleList.size()][4];

        
        for(int i = 0; i < AllSalesmenSaleList.size(); i++){
          
            rows[i][0] = AllSalesmenSaleList.get(i).getEmail();
            rows[i][1] = AllSalesmenSaleList.get(i).getTotalSales();
            rows[i][2] = AllSalesmenSaleList.get(i).getSalesTarget();
            
          
        }
        
        DefaultTableModel mmodel = new DefaultTableModel(rows, colNames);
        jTable_Approval.setModel(mmodel);
        jTable_Approval.setRowHeight(45);
       
        
        
        
    }

