    public String Topsalesmen(){
       String salesmen = null;
        Connection con = My_CNX.getConnection(); // connection is called
        PreparedStatement ps;
        ResultSet rs;
        
        /*
        To display the name of the salesmen instead of their email, 
        the email is used as a parameter int he SQL query and the corresponding name
        is displayed in the label
        */
        try {
            ps = con.prepareStatement("SELECT * FROM `users` WHERE `Email` = ?"); //query
            String Ema = String.valueOf(jTable_Approval.getValueAt(0,0)); //getting the email of top salesmen
            ps.setString(1, Ema);
            rs = ps.executeQuery();//executing the query
            if(rs.next()){
                salesmen= rs.getString("FirstName")+ " " + rs.getString("LastName"); //combining the first and last name of salesmen
        }
        } catch (SQLException ex) {
            Logger.getLogger(Admin_ViewSalesmenRecords.class.getName()).log(Level.SEVERE, null, ex);
        }
        return salesmen;
    }//GEN-LAST:event_jButton_ViewTopSalesmenActionPerformed

