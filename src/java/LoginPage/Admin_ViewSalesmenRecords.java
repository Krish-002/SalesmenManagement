    private void jTable_ApprovalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ApprovalMouseClicked
      
        //As a row is selected in the table, the respective bonus for the salesmen is shown
        Integer rowIndex = jTable_Approval.getSelectedRow();
        int TS = Integer.parseInt(jTable_Approval.getValueAt(rowIndex, 2).toString());
        int ST = Integer.parseInt(jTable_Approval.getValueAt(rowIndex, 1).toString());
        int Bonus;
        
        //mathematical calculations for bonus based on sales and target
        Bonus = Math.abs((TS - ST)/10);
        jTextField_Email.setText(jTable_Approval.getValueAt(rowIndex, 0).toString());
        jLabel_Bonus.setText((String.valueOf(Bonus)));
        
    }//GEN-LAST:event_jTable_ApprovalMouseClicked

