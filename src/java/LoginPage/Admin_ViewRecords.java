     // function to check empty fields
    public boolean verifFields()
    {
        /*
        Verify method that checks for the various validations
        */
        if(jTextField_Fname.getText().equals("") || jTextField_Lname.getText().equals("") || jTextField_Utype.getText().equals("") || jTextField_Approval.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty", "Empty Fields", 0);
            return false;
        }else{
            return true;
        }
    }
    
