    private void jTable_ApprovalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_ApprovalMouseClicked
        /*
        Enables the functionality of transfering the values from the table
        to the text fields
        */
        Integer rowIndex = jTable_Approval.getSelectedRow();
        jTextField_Fname.setText(jTable_Approval.getValueAt(rowIndex, 0).toString());
        jTextField_Lname.setText(jTable_Approval.getValueAt(rowIndex, 1).toString());
        jTextField_Email.setText(jTable_Approval.getValueAt(rowIndex, 2).toString());
        jTextField_Pass.setText(jTable_Approval.getValueAt(rowIndex, 3).toString());
        jTextField_Tel.setText(jTable_Approval.getValueAt(rowIndex, 4).toString());
        jTextField_Utype.setText(jTable_Approval.getValueAt(rowIndex, 5).toString());
        jTextField_Approval.setText(jTable_Approval.getValueAt(rowIndex, 7).toString());
        
        
    }//GEN-LAST:event_jTable_ApprovalMouseClicked

