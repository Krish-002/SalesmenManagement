    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        /*
        The update functionality: uses the method created in C_Users class 
        by getting the information from the invisible text fields and the fields
        edited by the user
        */
        try{
            
            if(verifFields())
            {
                String FirstName = jTextField_Fname.getText();
                String LastName = jTextField_Lname.getText();
                String UserType = jTextField_Utype.getText();
                String Approval = jTextField_Approval.getText();
                String Email = jTextField_Email.getText();
                String Telephone = jTextField_Tel.getText();
                String Password = jTextField_Pass.getText();


                // username`, `password`, `user_type`, `fullname`, `tel`, `email
                C_Users user = new C_Users(FirstName, LastName, Email, Password, Telephone, UserType, null, Approval);
                C_Users.updateUser(user);
                populateJtable();
            }
            
        }
        catch(Exception ex){
                JOptionPane.showMessageDialog(null, "Select a User Before Editing", "No User Selected", 1);
                }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

