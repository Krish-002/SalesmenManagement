    public void populateJtable()
    {
        /*
        Calls the method created in C_Users class and uses it create an array list
        that shows pending users first and the approved users
        Using that, I filled the JTable as shown below
        */
    C_Users users = new C_Users();
        ArrayList<C_Users> userList = users.UsersList();
        
        String[] colNames = {"FirstName","LastName","Email","Password","Telephone","UserType","Photograph","Approval"};
        Object[][] rows = new Object[userList.size()][8];

        
        for(int i = 0; i < userList.size(); i++){
          
            rows[i][0] = userList.get(i).getFirstName();
            rows[i][1] = userList.get(i).getLastName();
            rows[i][2] = userList.get(i).getEmail();
            rows[i][3] = userList.get(i).getPassword();
            rows[i][4] = userList.get(i).getTelephone();
            rows[i][5] = userList.get(i).getUserType();
            rows[i][6] = userList.get(i).getPhotograph();
            rows[i][7] = userList.get(i).getApproval();
          
        }
        
        DefaultTableModel mmodel = new DefaultTableModel(rows, colNames);
        jTable_Approval.setModel(mmodel);
        jTable_Approval.setRowHeight(45);
    }

