    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        try{
            {
                //update
                String SalesmenEmail = TF_SalesmenEmail.getText();
                String OrderStatus = TF_OrderStatus.getText();
                int ID = Integer.parseInt(OrderID_TF.getText());
                String DateOfSales = Date_TF.getText();
                String ProductDetails = jTextField_Email.getText();
                String Brand = Brand_TF.getText();
                int Quantity = Integer.parseInt(Quantity_TF.getText());
                String RetailerEmail = TF_RE.getText();


                // username`, `password`, `user_type`, `fullname`, `tel`, `email
                RetailerOrder_DB Order = new RetailerOrder_DB(ID, SalesmenEmail,
                        DateOfSales, Quantity, Brand, ProductDetails, RetailerEmail, OrderStatus);
                RetailerOrder_DB.UpdateOrderStatus(Order);
                populateJtable();
                
            }

        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Select an Order Before Editing", "No User Selected", 1);
        }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

