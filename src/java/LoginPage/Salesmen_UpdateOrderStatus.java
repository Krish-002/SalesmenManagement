     public void populateJtable()
    {
        String Status = (String) CB_Sort.getSelectedItem();//convert object to string
        String Email = (String) Salesmen_Combobox.getSelectedItem();
        
//using the methods defined in RetailerOrder_DB to show update order status, inputs used to run query
        RetailerOrder_DB Order = new RetailerOrder_DB();
        ArrayList<RetailerOrder_DB> SalesmenOrderList = Order.SalesmenOrderList(Status, Email);
        
        String[] colNames = {"Order ID", "Salesmen Email","Date","Quantity","Brand",
            "Product Details","Retailer Email","Order Status"};
        Object[][] rows = new Object[SalesmenOrderList.size()][8];
        
        for(int i = 0; i < SalesmenOrderList.size(); i++){
          
            rows[i][0] = SalesmenOrderList.get(i).getID();
            rows[i][1] = SalesmenOrderList.get(i).getSalesmenEmail();
            rows[i][2] = SalesmenOrderList.get(i).getDateOfSale();
            rows[i][3] = SalesmenOrderList.get(i).getQuantity();
            rows[i][4] = SalesmenOrderList.get(i).getBrand();
            rows[i][5] = SalesmenOrderList.get(i).getProductDetails();
            rows[i][6] = SalesmenOrderList.get(i).getRetailerEmail();
            rows[i][7] = SalesmenOrderList.get(i).getOrderStatus();
        }
        DefaultTableModel mmodel = new DefaultTableModel(rows, colNames);
        jTable_Approval.setModel(mmodel);
        jTable_Approval.setRowHeight(45);
    }
     
